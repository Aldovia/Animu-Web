import 'dart:convert';

class Command {
  final String name;
  final List<String> aliases;
  final List<String> requiredPermissions;
  final String category;
  final String description;
  final String extendedHelp;
  final bool nsfw;
  final int permissionLevel;
  final List<String> runIn;
  final String usageString;

  Command({
    this.name,
    this.aliases,
    this.requiredPermissions,
    this.category,
    this.description,
    this.extendedHelp,
    this.nsfw,
    this.permissionLevel,
    this.runIn,
    this.usageString,
  });

  static fetchAllCommands() {
    final Map<String, dynamic> commandsParsed = json.decode(commandsString);

    final List<Command> commands = [];

    for (int i = 0; i < commandsParsed['commands'].length; i++) {
      final commandJson = commandsParsed['commands'][i];
      commands.add(
        Command(
          name: commandJson['name'],
          aliases: commandJson['aliases'].cast<String>(),
          requiredPermissions:
              commandJson['requiredPermissions'].cast<String>(),
          category: commandJson['category'],
          description: commandJson['description'],
          extendedHelp: commandJson['extendedHelp'],
          nsfw: commandJson['nsfw'],
          permissionLevel: commandJson['permissionLevel'],
          runIn: commandJson['runIn'].cast<String>(),
          usageString: commandJson['usage']['nearlyFullUsage'],
        ),
      );
    }

    return commands;
  }
}

